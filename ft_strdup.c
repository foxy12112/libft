#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char		*dest;
	size_t		count;
	size_t		i;

	i = 0;
	count = 0;
	while (s1[i])
		i++;
	dest = (char *)malloc(sizeof(*dest) * i + 1);
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
