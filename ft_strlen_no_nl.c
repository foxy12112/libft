#include "libft.h"

int ft_strlen_no_nl(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i + 1] == '\0')
			break;
		if (str[i] == '\n')
			continue;
		i++;
	}
	return (i);
}